sealed class SegmentType() {
    object Dots : SegmentType()
    class Number(val value: Int) : SegmentType()
}

fun getLittleClockStates(segmentType: SegmentType): List<LittleClockElementState> {
    when (segmentType) {
        SegmentType.Dots -> return dots
        is SegmentType.Number -> {
            if (segmentType.value !in 0..9) return unknown
            return numberStatesMap[segmentType.value] ?: unknown
        }
    }

}